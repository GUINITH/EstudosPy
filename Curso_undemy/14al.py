# input: É uma função que recebe dados digitado pelos Usuários. Podemos receber mais dados e guardar em variáveis diferentes. Como input nativamente  recebe uma string, podemos converter os dados recebidos para outro tipo de dado.
# Exemplo:

idade = input("Digite sua idade: ")
convert_idade = int(idade)
print("Sua idade é", convert_idade, "anos")

# output: Sua idade é 24 anos