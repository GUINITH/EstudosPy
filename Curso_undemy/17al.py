# Exerciicos de Operadors de comparação e condicionais


primeiro_valor =  input('Digite um valor: ')
segundo_valor = input('Digite outro valor: ')

if primeiro_valor > segundo_valor:
    print(f'Primeiro_valor={primeiro_valor} maior que segundo_valor={segundo_valor}')
elif primeiro_valor < segundo_valor:
    print(f'Primeiro_valor={primeiro_valor} menor que segundo_valor={segundo_valor}')
else:
    print(f'Primeiro_valor={primeiro_valor} igual ao segundo_valor={segundo_valor}')
