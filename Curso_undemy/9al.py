# Operadores Aritiméticos: adição, subitração, multiplicação, divisão e potência

# Adição: +
# Subitração: -
# Multiplicação: *
# Divisão_flutuante: /
# Divisão_inteira: //
# Resto da Divisão: %
# Potência: **

# Exemplos: 

print(10 + 5) # 15
print(10 - 5) # 5
print(10 * 5) # 50
print(10 / 5) # 2.0
print(10 // 5) # 2
print(10 % 5) # 0
print(10 ** 2) # 100