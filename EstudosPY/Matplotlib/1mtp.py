""" 
-  O que é Matplotlib?
Matplotlib é uma biblioteca de plotagem de gráficos de baixo nível em Python que serve como um utilitário de visualização.

Matplotlib foi criado por John D. Hunter.

Matplotlib é de código aberto e podemos usá-lo livremente.

Matplotlib é escrito principalmente em python, alguns segmentos são escritos em C, Objective-C e Javascript para compatibilidade de plataforma.
----------
Instalação do Matplotlib
Se você já possui Python e PIP instalados em um sistema, a instalação do Matplotlib é muito fácil.

Instale-o usando este comando:

pip install matplotlib

Importar o Matplotlib

Depois que Matplotlib estiver instalado, importe-o em seus aplicativos adicionando a instrução: import module

import matplotlib

"""

