"""
Tipos Int e Float
int => Numeros inteiros
float => Numeros reais
O tipo int representa qualquer número positivo ou negativo. Sem sinal e considerado positivo. 
O tipo float representa qualquer número real, positivo ou negativo. Float sem sinal é considerado positivo.

"""
# int 
print(1)
print(-2)
print(0)
# float
print(1.0)
print(-2.0)
print(0.0)
# A função type() exibe o tipo que o Python inferiu o valor.

print(type(1)) # int
print(type(1.0)) # float


