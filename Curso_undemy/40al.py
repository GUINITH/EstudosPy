"""
split e join com lista e str 
join une uma string 
split separa uma string e retorna uma lista
strip retira os espacos em branco no inicio e no final

"""

frase = '  Olha que coisa mais linda tão cheia de graça   '
frase2 = 'A coisa mais linda que ja vi passar'
print(frase.join(frase2))
print(frase.strip())
print(frase.split())