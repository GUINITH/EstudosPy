"""
Operadores de comparação: São operadores que comparam dois valores e o  sistema retorna um booleano (True ou False).

Operadores de comparação são usados ​para comparar dois valores:

- `==` : Igual a
- `!=` : Diferente de
- `>` : Maior que 
- `<` : Menor que 
- `=<` : Menor ou igual que
- `>=` : Maior ou igual que

"""

print(23 == 23) # True
print(23 != 23) # False
print(23 > 23) # False
print(23 < 23) # False
print(23 >= 23) # True
print(23 <= 23) # True 
